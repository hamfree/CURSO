package com.spring.bean06;

public class PlazaGaraje
{
    private final int planta;
    private final int plaza;

    public PlazaGaraje( int planta, int plaza )
    {
        this.planta = planta;
        this.plaza = plaza;
    }

    public int getPlanta()
    {
        return planta;
    }

    public int getPlaza()
    {
        return plaza;
    }

}
